public class OperasiMatika {
    public static void main(String[] args) {
        int a = 100;
        int b = 500;
        int aa = 11;
        int c = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(b / a);
        System.out.println(aa % c);
        

        int d = 10;
        d += 100;
        d -= 10;
        d /= 2;
        System.out.println(d);
        System.out.println("+++++++++++++++++++++");
        // ++ dan --
        int e = +100;
        e++;
        System.out.println(e);
        e--;
        System.out.println(e);
        // operator perbadningan
        System.out.println("++++++++++++++++");
        int value1 = 100;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);





    }
}
