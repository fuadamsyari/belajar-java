public class Array2dimensi {
    public static void main(String[] args) {
        
        // array 2 dimensi
        String[][] Mahasiswa = {
        {"fuad", "dodit", "galih", "Edo"},
        {"Risa", "Nuri", "Nana", "Eudora"},
        };


        System.out.println("baris 1 kolom 1 " + Mahasiswa[0][0]);
        System.out.println("baris 2 kolom 2 " + Mahasiswa[1][1]);
        System.out.println("baris 1 kolom 4 " + Mahasiswa[0][3]);
        System.out.println("baris 2 kolom 4 " + Mahasiswa[1][3]);

    }
}
