public class Variabel {
    public static void main(String[] args) {
        // Variabel
        String name;
        name = "Ahmad Fuad Amsyari";
        System.out.println(name);

        // langsung inisiasi
        int Usia = 21;
        String alamat = "Indonesia";
        System.out.println(Usia + alamat);

        name = "Minurisah";
        System.out.println(name);

        // Tipe data Variabel menggunakan Var
        // harus tetap pakai iniasi terlebih dahulu
        var youtube = "Fuad amsyari";
        System.out.println(youtube);

        // Tipe Final
        final String application = "Belajar Java";
        System.out.println(application);
        






    }
}
